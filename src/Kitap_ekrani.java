    private void kitap_ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kitap_ekleActionPerformed

        mesaj_yazisi.setText("");

        String kitapAd = kitap_adi_alani.getText();
        String kitapAltAd = kitap_alt_adi_alani.getText();
        String kitapSeriAd = kitap_seri_adi_alani.getText();
        String yazar = yazar_alani.getText();
        String yayınEvi = yayın_evi_alani.getText();
        String Isbn_no = Isbn_no_alani.getText();
        int IsbnNo = Integer.parseInt(Isbn_no);
        String aciklama = aciklama_alani.getText();

        islemler.kitapEkle(kitapAd,kitapAltAd,kitapSeriAd,yazar,yayınEvi,IsbnNo,aciklama);

        kitapGoruntule();

        mesaj_yazisi.setText("Yeni kitap başarıyla eklendi");

    }//GEN-LAST:event_kitap_ekleActionPerformed

