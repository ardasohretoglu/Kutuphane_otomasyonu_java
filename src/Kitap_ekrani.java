    public void kitapGoruntule(){
        
        
        model.setRowCount(0);
        
        ArrayList<Kitap> kitapList = new ArrayList<Kitap>();
        
        kitapList = islemler.kitapGetir();
        
        if (kitapList!=null){
            
            for(Kitap kitap : kitapList){
                
                Object[] eklenecek = {kitap.getId(),kitap.getKitap_adı(),kitap.getKitap_alt_adı(),kitap.getKitap_seri_adı(),kitap.getYazar(),kitap.getYayın_evi(),kitap.getIsbn_no(),kitap.getAciklama()};
                
                model.addRow(eklenecek);
                
            }
            
        }
        
        
        
    }
    
